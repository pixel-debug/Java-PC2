    private void botaoLetraActionPerformed(java.awt.event.ActionEvent evt) {
        String letra = ((JToggleButton) evt.getSource()).getText().toLowerCase();
        ((JToggleButton) evt.getSource()).setEnabled(true);
        ArrayList<Integer> posi = jogo.verificaLetra(letra);
        jLabel2.setText(String.valueOf(jogo.getNumTentantivas()));
        if( jogo.getNumTentantivas() < 0){
           JOptionPane.showMessageDialog(this, "Você estourou as tentativas", "Perdeu", JOptionPane.PLAIN_MESSAGE);
           limpaPainel();
           reinicia();
        }
        if (posi.size() == 0) {
            JOptionPane.showMessageDialog(this, "Letra inexistente", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            colocaLetra(letra, posi);
        }
    }

    private void novoJogoActionPerformed(java.awt.event.ActionEvent evt) {
        limpaPainel();
        reinicia();
    }

    private void sairActionPerformed(java.awt.event.ActionEvent evt) {
        JOptionPane.showMessageDialog(this, "Bye bye...", "Saída", JOptionPane.PLAIN_MESSAGE);
        System.exit(1);
    }

    private void mostrarPalavraActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        if (jogo.resultado()) {
            JOptionPane.showMessageDialog(this, "Temos um vencedor", "Parabés", JOptionPane.PLAIN_MESSAGE);
            limpaPainel();
        }
        if (!jogo.resultado()) {
            JOptionPane.showMessageDialog(this, "Temos um perdedor", "Tente novamente", JOptionPane.PLAIN_MESSAGE);
            limpaPainel();
        }
    }

