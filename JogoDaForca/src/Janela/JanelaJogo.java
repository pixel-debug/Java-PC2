    public void limpaPainel(){
        for (int i = 0; i < painel.length;i++){
            painel[i].setText(" ");
        }
    }
     
    public void colocaLetra(String letra, ArrayList<Integer> posicao) {
        Iterator<Integer> posicoes = posicao.iterator();
        while (posicoes.hasNext()) {
            int aux = posicoes.next();
            painel[aux].setText(letra.toUpperCase());
            painel[aux].setHorizontalAlignment(JTextField.CENTER);
        }
    }

